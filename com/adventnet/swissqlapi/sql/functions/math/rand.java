    @Override
    public void toTimesTen(final SelectQueryStatement to_sqs, final SelectQueryStatement from_sqs) throws ConvertException {
        this.functionName.setColumnName("RAND");
        final Vector arguments = new Vector();
        if (this.functionArguments.size() == 0) {
            this.functionName.setColumnName("");
            this.setOpenBracesForFunctionNameRequired(false);
            arguments.add(Math.random() + "");
            this.setFunctionArguments(arguments);
            return;
        }
        throw new ConvertException("\nThe function RAND is not supported in TimesTen 5.1.21\n");
    }
    
