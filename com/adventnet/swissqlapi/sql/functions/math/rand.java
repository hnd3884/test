    @Override
    public void toPostgreSQL(final SelectQueryStatement to_sqs, final SelectQueryStatement from_sqs) throws ConvertException {
        this.functionName.setColumnName("RANDOM");
        final Vector arguments = new Vector();
        for (int i_count = 0; i_count < this.functionArguments.size(); ++i_count) {
            if (this.functionArguments.elementAt(i_count) instanceof SelectColumn) {
                arguments.addElement(this.functionArguments.elementAt(i_count).toPostgreSQLSelect(to_sqs, from_sqs));
            }
            else {
                arguments.addElement(this.functionArguments.elementAt(i_count));
            }
        }
        this.setFunctionArguments(arguments);
        if (!arguments.isEmpty()) {
            String query = "(RANDOM() * GREATEST(1," + arguments.get(0).toString() + "))";
            if (from_sqs != null && !from_sqs.isAmazonRedShift() && from_sqs.canUseUDFFunctionsForNumeric()) {
                query = "(RANDOM() * GREATEST(1,TONUMERIC_UDF(" + arguments.get(0).toString() + ")))";
            }
            this.functionName.setColumnName(query);
            this.setOpenBracesForFunctionNameRequired(false);
            this.setFunctionArguments(new Vector());
        }
    }
    
