    public void toOracle(final SelectQueryStatement to_sqs, final SelectQueryStatement from_sqs) throws ConvertException {
        final Vector arguments = new Vector();
        for (int i_count = 0; i_count < this.functionArguments.size(); ++i_count) {
            if (this.functionArguments.elementAt(i_count) instanceof SelectColumn) {
                arguments.addElement(this.functionArguments.elementAt(i_count).toOracleSelect(to_sqs, from_sqs));
            }
            else {
                arguments.addElement(this.functionArguments.elementAt(i_count));
            }
        }
        this.setFunctionArguments(arguments);
        if (this.functionArguments.size() == 2) {
            this.functionName.setColumnName("DECODE");
            final Object param1 = this.functionArguments.get(0);
            final Object param2 = this.functionArguments.get(1);
            this.functionArguments.setSize(1);
            final StringBuffer argBuffer = new StringBuffer();
            argBuffer.append(param1.toString());
            argBuffer.append(" , ");
            argBuffer.append(param2.toString());
            argBuffer.append(" , NULL , ");
            argBuffer.append(param1.toString());
            final Vector arg = new Vector();
            arg.add(argBuffer.toString());
            this.setFunctionArguments(arg);
        }
    }
    
